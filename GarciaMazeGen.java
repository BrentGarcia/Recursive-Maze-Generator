    /**
     * Creates a random maze in a 2D array.
     * 
     * @see <a href="http://en.wikipedia.org/wiki/Maze_generation_algorithm#Recursive_division_method">Recursive_division_method</a>
     * @return 2D array containing a maze
     */
    private static char[][] makeMaze()
    {
        char level[][] = createBlankLevel();
        
        makeMazeRecursive(level, 1, 1, LEVEL_WIDTH-2, LEVEL_HEIGHT-2); //TODO: may need to change but probably not.
        
        return level;
    }

    /**
     * Creates an empty level of standard level height and width. Level will be
     * blank but bordered with wall characters.
     * 
     * @return  2D array containing a maze
     */
    private static char[][] createBlankLevel()
    {   
        char level[][] = new char[LEVEL_HEIGHT][LEVEL_WIDTH];
        
        //reset level to be entirely blank
        for (int y = 0; y < LEVEL_HEIGHT; y++) 
            for (int x = 0; x < LEVEL_WIDTH; x++)
                level[y][x] = ICON_BLANK;
    
        //top barrier   
        for (int x = 0; x < LEVEL_WIDTH; x++)
            level[0][x] = ICON_WALL;
    
        //bottom barrier
        for (int x = 0; x < LEVEL_WIDTH; x++)
            level[LEVEL_HEIGHT-1][x] = ICON_WALL;
    
        //left barrier
        for (int y = 0; y < LEVEL_HEIGHT; y++)
            level[y][0] = ICON_WALL;
    
        //right barrier
        for (int y = 0; y < LEVEL_HEIGHT; y++)
            level[y][LEVEL_WIDTH-1] = ICON_WALL;
            
        return level;
    }

    /*
     * Recursive method to fill maze subsections.
     * 
     * Written By Brent Garcia.
     */
    private static void makeMazeRecursive(char[][]level, int startX, int startY, int endX, int endY)    {
    	
    	
    	if(endX-startX < 3 || endY-startY < 3) return;
    	
    	int randRow = randBetween(startY+1, endY-2);
    	int randCol = randBetween(startX+1, endX-2);    	
    	
    	// Horizontal Barrier
        for (int x = 1; x <= endX; x++) {       	
        		level[randRow][x] = ICON_WALL;  	
        }
                  
        // Vertical Barrier
        for (int y = 1; y <= endY; y++) {
        		level[y][randCol] = ICON_WALL;       	       	
        }
        
        //TOP WALL HOLE
        level[randBetween((startY),(randRow-1))][randCol] = ICON_BLANK;
        
        //LEFT WALL HOLE
        level[randRow][randBetween(startX,(randCol-1))] = ICON_BLANK;
        
        //RIGHT WALL HOLE
        level[randRow][randBetween((randCol+1),endX)] = ICON_BLANK;
        
        //BOT WALL HOLE
        level[randBetween((randRow+1),endY)][randCol] = ICON_BLANK;
            
        makeMazeRecursive(level, startX, startY, randCol-1, randRow-1); //TOP LEFT
        makeMazeRecursive(level, randCol+1,startY,endX,randRow-1); //TOP RIGHT
        makeMazeRecursive(level,startX,randRow+1,randCol-1,endY); // BOT LEFT
        makeMazeRecursive(level,randCol+1,randRow+1,endX,endY); // BOT RIGHT
    }
    
    public static int randBetween(int min, int max) {
    	int randomNum = ThreadLocalRandom.current().nextInt(min, max + 1);
    	return randomNum;
    }
    
